cantidad = int(input())
datos = []

for i in range(cantidad):
    dato = input()
    datos.append(dato)

datos_ordenados = sorted(datos)
print(datos_ordenados)

#en el primer espacio se escriben la cantidad de datos que va a ingresar
#luego de eso damos un enter y empezamos a escribir nuestros datos siempre dando enter al finalizar cada uno
#por último este nos mostrará el resultado en la lista

